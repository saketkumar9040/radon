const mongoose = require("mongoose");

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////    SCHEMA    OF   AUTHOR     //////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const authorSchema = new mongoose.Schema(
  {
    fname: {
      type: String,
      required: true,
      trim: true,
      match: [
        /^[A-Za-z. ]{2,30}$/,
        "lname Should Be In Alphabets & Minimum Length Should Be 2 ",
      ],
    },
    lname: {
      type: String,
      required: true,
      match: [
        /^[A-Za-z. ]{2,30}$/,
        "lname Should Be In Alphabets & Minimum Length Should Be 2 ",
      ],
    },
    title: {
      type: String,
      required: true,
      enum: ["Mr", "Mrs", "Miss"],
    },
    email: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
      match: [
        /^(?=.*[0-9])(?=.*[!@#$%^&*])[a-zA-Z0-9!@#$%^&*]{8,16}$/,
        "password Must Contain One Uppercase,Lowercase,Number,Symbol And Minimum Length Should Be 8-Character",
      ]
    },
  },
  {
    timestamps: true,
  }
);

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////    EXPORTING   MODULES    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


module.exports = mongoose.model("Author", authorSchema);
