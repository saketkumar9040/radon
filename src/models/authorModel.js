const mongoose = require("mongoose");

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////    SCHEMA    OF   AUTHOR     //////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const authorSchema = new mongoose.Schema(
  {
    fname: {
      type: String,
      required: true,
      trim: true
    },
    lname: {
      type: String,
      required: true
    },
    title: {
      type: String,
      required: true,
      enum: ["Mr", "Mrs", "Miss"],
    },
    email: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    password: {
      type: String,
      required: true
    },
  },
  {
    timestamps: true,
  }
);

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////    EXPORTING   MODULES    /////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

module.exports = mongoose.model("Author", authorSchema);
